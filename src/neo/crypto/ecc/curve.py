"""
Elliptic Curve definitions.

Reference: Neo.Cryptography.ECC.ECCurve
"""

from dataclasses import dataclass


@dataclass(frozen=True)
class ECCurve:
    """Elliptic curve parameters."""
    name: str
    p: int  # Prime
    a: int  # Curve coefficient a
    b: int  # Curve coefficient b
    n: int  # Order
    gx: int  # Generator x
    gy: int  # Generator y


